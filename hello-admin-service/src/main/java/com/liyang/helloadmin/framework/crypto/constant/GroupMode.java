package com.liyang.helloadmin.framework.crypto.constant;

/**
 * @author cn-liyang
 * 分组模式
 */
public enum GroupMode {
    /**
     * Electronic Code Book
     */
    ECB,
    /**
     * Cipher Block Chaining
     */
    CBC,
    /**
     * Cipher Feedback
     */
    CFB,
    /**
     * Output Feedback
     */
    OFB,
    /**
     * Counter
     */
    CTR,
    /**
     * Galois/Counter Mode
     */
    GCM,
}
